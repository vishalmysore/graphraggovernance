/**
 * 9. Web Search — Wikipedia API + Bing scraping, returns combined text
 */
app.post('/api/web-search', async (req, res) => {
    const { query, maxPages = 3 } = req.body;
    if (!query) return res.status(400).json({ success: false, error: 'query is required' });

    const browserHeaders = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
        'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
        'Accept-Language': 'en-US,en;q=0.9',
    };

    const pages = [];

    const wikiHeaders = {
        'User-Agent': 'GraphRAGGovernance/1.0 (https://github.com/graphrag; research tool) axios/1.x',
        'Accept': 'application/json'
    };

    // --- Strategy 1: Wikipedia search + article extract (server-side, no origin param) ---
    try {
        const wikiSearch = await axios.get('https://en.wikipedia.org/w/api.php', {
            params: { action: 'query', list: 'search', srsearch: query, format: 'json', srlimit: 3 },
            headers: wikiHeaders,
            timeout: 10000
        });
        const wikiResults = wikiSearch.data?.query?.search || [];
        console.log(`Wikipedia returned ${wikiResults.length} results for "${query}"`);

        for (const result of wikiResults.slice(0, 2)) {
            try {
                const articleResp = await axios.get('https://en.wikipedia.org/w/api.php', {
                    params: { action: 'query', prop: 'extracts', explaintext: true, exlimit: 1, titles: result.title, format: 'json' },
                    headers: wikiHeaders,
                    timeout: 10000
                });
                const pagesObj = articleResp.data?.query?.pages || {};
                const page = Object.values(pagesObj)[0];
                const text = (page?.extract || '').substring(0, 5000);
                if (text.length > 100) {
                    pages.push({
                        url: `https://en.wikipedia.org/wiki/${encodeURIComponent(result.title.replace(/ /g, '_'))}`,
                        title: result.title,
                        text
                    });
                }
            } catch (e) {
                console.warn(`Wikipedia article fetch failed for "${result.title}": ${e.message}`);
            }
        }
    } catch (e) {
        console.warn('Wikipedia search failed:', e.message);
    }

    // --- Strategy 2: DuckDuckGo HTML search + fetch pages ---
    if (pages.length < maxPages) {
        try {
            // Use POST form to DDG HTML (more reliable than lite)
            const ddgResp = await axios.post(
                'https://html.duckduckgo.com/html/',
                `q=${encodeURIComponent(query)}&b=&kl=&df=`,
                { headers: { ...browserHeaders, 'Content-Type': 'application/x-www-form-urlencoded' }, timeout: 12000 }
            );
            const $ = cheerio.load(ddgResp.data);

            const urlsFound = [];
            // DDG HTML results: .result__a links, or .result__url
            $('a.result__a').each((i, el) => {
                if (urlsFound.length >= 5) return;
                const href = $(el).attr('href');
                const title = $(el).text().trim();
                // DDG wraps URLs in a redirect — extract actual URL
                if (href && title.length > 5) {
                    const urlMatch = href.match(/uddg=([^&]+)/);
                    const actualUrl = urlMatch ? decodeURIComponent(urlMatch[1]) : href;
                    if (actualUrl.startsWith('http') && !actualUrl.includes('duckduckgo.com')) {
                        urlsFound.push({ url: actualUrl, title });
                    }
                }
            });

            console.log(`DDG HTML found ${urlsFound.length} URLs for "${query}"`);

            for (const result of urlsFound.slice(0, maxPages - pages.length)) {
                try {
                    const pageResp = await axios.get(result.url, { headers: browserHeaders, timeout: 8000, maxRedirects: 3 });
                    const $p = cheerio.load(pageResp.data);
                    $p('script, style, nav, header, footer, aside, iframe, noscript').remove();
                    const mainText = ($p('article, main, [role="main"], .content, #content').text()
                        || $p('body').text()).replace(/\s+/g, ' ').trim().substring(0, 4000);
                    if (mainText.length > 200) {
                        pages.push({ url: result.url, title: result.title, text: mainText });
                    }
                } catch (e) {
                    console.warn(`Page fetch failed for ${result.url}: ${e.message}`);
                }
            }
        } catch (e) {
            console.warn('DDG search failed:', e.message);
        }
    }

    console.log(`🔍 "${query}": returning ${pages.length} sources`);
    const combinedText = pages.map(p => `## ${p.title}\nSource: ${p.url}\n\n${p.text}`).join('\n\n---\n\n');
    res.json({ success: true, query, pages: pages.map(p => ({ url: p.url, title: p.title })), combinedText });
});

