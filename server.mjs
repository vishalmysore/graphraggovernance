/**
 * 1. Return spec text (and cached graph if available) — no LLM
 */
app.get('/api/get-spec', (req, res) => {
    try {
        const standard = req.query.standard;
        if (!standard) return res.status(400).json({ success: false, error: 'standard is required' });

        const safeName = standard.toLowerCase().replace(/\s/g, '_');
        const graphPath = path.join(GRAPH_DIR, `${safeName}_base.json`);
        const schemaPath = path.join(SCHEMA_DIR, `${safeName}_schema.json`);

        // Return cached graph if available
        if (fs.existsSync(graphPath) && fs.existsSync(schemaPath)) {
            const graph = JSON.parse(fs.readFileSync(graphPath, 'utf8'));
            const schema = JSON.parse(fs.readFileSync(schemaPath, 'utf8'));
            return res.json({ success: true, cached: true, graph, schema });
        }

        const specFile = path.join(SPEC_DIR, `${safeName}.txt`);
        if (!fs.existsSync(specFile)) {
            return res.status(404).json({ success: false, error: `No spec found for ${standard} in /specs folder.` });
        }

        const specText = fs.readFileSync(specFile, 'utf8');
        res.json({ success: true, cached: false, specText });
    } catch (error) {
        res.status(500).json({ success: false, error: error.message });
    }
});

/**
 * 2. Return cached schema for a standard
 */
app.get('/api/get-schema', (req, res) => {
    try {
        const standard = req.query.standard;
        const safeName = (standard || '').toLowerCase().replace(/\s/g, '_');
        const schemaPath = path.join(SCHEMA_DIR, `${safeName}_schema.json`);
        if (!fs.existsSync(schemaPath)) return res.json({ success: true, schema: null });
        const schema = JSON.parse(fs.readFileSync(schemaPath, 'utf8'));
        res.json({ success: true, schema });
    } catch (error) {
        res.status(500).json({ success: false, error: error.message });
    }
});

/**
 * 3. Cache graph + schema generated by frontend WebLLM
 */
app.post('/api/cache-graph', (req, res) => {
    try {
        const { standard, graph, schema } = req.body;
        const safeName = standard.toLowerCase().replace(/\s/g, '_');
        fs.writeFileSync(path.join(GRAPH_DIR, `${safeName}_base.json`), JSON.stringify(graph, null, 2));
        fs.writeFileSync(path.join(SCHEMA_DIR, `${safeName}_schema.json`), JSON.stringify(schema, null, 2));
        res.json({ success: true });
    } catch (error) {
        res.status(500).json({ success: false, error: error.message });
    }
});

/**
 * 4. Parse PDF — return extracted text only, no LLM
 */
app.post('/api/parse-pdf', upload.single('file'), async (req, res) => {
    try {
        const dataBuffer = fs.readFileSync(req.file.path);
        const pdfData = await pdf(dataBuffer);
        fs.unlinkSync(req.file.path);
        console.log(`PDF parsed: ${pdfData.text?.length || 0} chars`);
        res.json({ success: true, text: pdfData.text });
    } catch (error) {
        if (req.file?.path && fs.existsSync(req.file.path)) fs.unlinkSync(req.file.path);
        res.status(500).json({ success: false, error: error.message });
    }
});

